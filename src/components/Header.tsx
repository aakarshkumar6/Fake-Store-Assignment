import { Link, useLocation } from 'react-router-dom';
import { ShoppingBag, Heart, Search } from 'lucide-react';
import { motion } from 'framer-motion';
import { useAppSelector } from '@/store/hooks';
import { selectFavoritesCount } from '@/store/slices/favoritesSlice';
import { cn } from '@/lib/utils';

export function Header() {
  const location = useLocation();
  const favoritesCount = useAppSelector(selectFavoritesCount);

  const navItems = [
    { path: '/', label: 'Products', icon: ShoppingBag },
    { path: '/favorites', label: 'Favorites', icon: Heart, count: favoritesCount },
  ];

  return (
    <header className="sticky top-0 z-50 w-full border-b bg-card/80 backdrop-blur-md">
      <div className="container flex h-16 items-center justify-between">
        <Link to="/" className="flex items-center gap-2">
          <motion.div
            whileHover={{ scale: 1.05 }}
            className="flex h-10 w-10 items-center justify-center rounded-lg bg-primary"
          >
            <ShoppingBag className="h-5 w-5 text-primary-foreground" />
          </motion.div>
          <span className="font-display text-xl font-semibold">Store</span>
        </Link>

        <nav className="flex items-center gap-1">
          {navItems.map((item) => {
            const Icon = item.icon;
            const isActive = location.pathname === item.path;
            
            return (
              <Link key={item.path} to={item.path}>
                <motion.div
                  whileHover={{ scale: 1.02 }}
                  whileTap={{ scale: 0.98 }}
                  className={cn(
                    "relative flex items-center gap-2 rounded-lg px-4 py-2 text-sm font-medium transition-colors",
                    isActive
                      ? "bg-primary text-primary-foreground"
                      : "text-muted-foreground hover:bg-secondary hover:text-foreground"
                  )}
                >
                  <Icon className="h-4 w-4" />
                  <span className="hidden sm:inline">{item.label}</span>
                  {item.count !== undefined && item.count > 0 && (
                    <motion.span
                      initial={{ scale: 0 }}
                      animate={{ scale: 1 }}
                      className="flex h-5 min-w-5 items-center justify-center rounded-full bg-favorite px-1.5 text-xs font-semibold text-favorite-foreground"
                    >
                      {item.count}
                    </motion.span>
                  )}
                </motion.div>
              </Link>
            );
          })}
        </nav>
      </div>
    </header>
  );
}
